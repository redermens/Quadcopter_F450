int recgImg(unsigned char *bmp, int H, int W, int L0, int L1, int L2)
{
	TRANSFORM_MAT_3X3 transform;
	int resH[4], resW[4], i, j, k, res, score[9];
	unsigned char tp;
	int maxm=-9999,maxi=0;

	//获得四角点坐标
	res = findCorner4(bmp,H,W,-1,L0,L1,L2,resH,resW);
	if(res<0) return -1;

	//透视采样
	quadrilateralToQuadrilateral(&transform, 0, 0, 40 - 1, 0, 0, 40 - 1, 40 - 1, 40 - 1,
		resW[0], resH[0], resW[1], resH[1], resW[2], resH[2], resW[3], resH[3]);
	k = 0;
	for (i = 0; i < 40; i++)
	{
		for (j = 0; j < 40; j++)
		{
			posY[k] = i;
			posX[k++] = j;
		}
	}
	transformPoints(transform, posX, posY, 1600);
	for (i = 1; i < 40 - 1; i++)
	{
		for (j = 1; j < 40 - 1; j++)
		{
			tp = bmp[(int)posY[i* 40 + j] * W + (int)posX[i* 40 + j]];//边缘做滤去处理
			tp = tp ? 1 : 0;
			mat[i * 40 + j] = tp;
		}
	}

	//识别
	for (i = 0; i < 9; i++) score[i] = 0;
	for (i = 0; i < 1600; i++)
	{
		if (mat[i] == md3[i]) score[3]++;
		if (mat[i] == md4[i]) score[4]++;
		if (mat[i] == md5[i]) score[5]++;
		if (mat[i] == md6[i]) score[6]++;
		if (mat[i] == md7[i]) score[7]++;
		if (mat[i] == md8[i]) score[8]++;
	}
	for (i = 3; i <= 8; i++)
	{
		if (score[i] > maxm)
		{
			maxi = i;
			maxm = score[i];
		}
	}
	return maxi;
}
